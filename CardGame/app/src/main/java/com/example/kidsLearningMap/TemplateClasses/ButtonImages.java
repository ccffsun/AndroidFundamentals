package com.example.kidsLearningMap.TemplateClasses;

public class ButtonImages {
    private int buttonImages;
    private String item;

    public ButtonImages(int buttonImages, String item) {
        this.buttonImages = buttonImages;
        this.item = item;
    }

    public int getButtonImages() {
        return buttonImages;
    }

    public void setButtonImages(int buttonImages) {
        this.buttonImages = buttonImages;
    }

    public String getItem() {
        return item;
    }

    public void setItem(String item) {
        this.item = item;
    }
}
